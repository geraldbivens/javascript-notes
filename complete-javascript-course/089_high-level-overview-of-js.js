// High-level
// Software programs require hardware resources to run (i.e., memory and the CPU). Whether these resources are allocated manually or automatically is the difference between a programming language being categorized as low-level or high-level. Low-level languages, like C, allow the developer to allocate these resources manually, while high-level languages, like JavaScript, allocate these resources automatically.

// Garbage collection
// Garbage collection is an algorithm in the JavaScript engine. It handles the allocation of memory by automatically removing old, unused objects. (Is this all that it does?)

// Interpreted or just-in-time compiled
// Computer processors only understand 0s and 1s, or machine code. Compiling is the process of converting the code written in a specific language to machine code so that it can be understood by the processor. In the case of JavaScript, this process takes place within the JavaScript engine, or behind the scenes. (Compiling is also referred to as 'interpreting.')

